//사용자에게 숫자 1개를 받습니다.
//해당 숫자가 짝수인지 홀수인지 확인하여, 짝수라면
//알림창에 "짝수" 출력!!! 만약 홀수라면 "홀수" 라고 출력하시오.
//단, 사용자가 취소 버튼을 클릭 할 수 있다는 것을 감안하여 예외 조항 처리를 반드시 해주세요!!!

// 사용자에게 숫자 입력을 받음
let userInput = prompt("숫자를 입력하세요:");

// 입력이 취소되었거나 빈 문자열인 경우 예외 처리
if (userInput === null || userInput === "") {
  alert("입력이 취소되었습니다.");
} else {
  // 입력 값을 숫자로 변환
  let number = parseInt(userInput, 10);

  // 변환된 값이 숫자인지 확인
  if (isNaN(number)) {
    alert("유효한 숫자를 입력하세요.");
  } else {
    // 삼항 조건 연산자를 사용하여 짝수인지 홀수인지 확인 후 알림창에 출력
    let result = (number % 2 === 0) ? "짝수" : "홀수";
    alert(result);
  }
}